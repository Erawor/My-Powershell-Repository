function Get-SQLData {

Param($strSQL)

$strConnection = "Data Source=$script:SQLServer; Initial Catalog=$script:DBName; Integrated Security=SSPI" 
$sqlConnection = New-Object System.Data.SqlClient.SqlConnection($strConnection) 

$sqlCommand = New-Object System.Data.SqlClient.sqlCommand
$sqlCommand.Connection = $sqlConnection
$DataSet = new-object System.Data.DataSet
$sqlDataAdapter = new-object System.Data.SqlClient.SqlDataAdapter($sqlCommand)

$sqlCommand.CommandText = $strSQL

[void]$sqlDataAdapter.Fill($DataSet,"DataSet")

$sqlConnection.close()

$colNames=$DataSet.tables[0].columns | foreach-object {$_.columnName}

if ($DataSet.tables[0].rows.count -gt 0) {

$DataSet.tables[0].rows | 

ForEach-Object {
        $Row = $_
        $PS = new-object psobject
        $colNames | 
            foreach-object {
	        $colName = $_ 
	        $PS = $PS | add-member -passthru NoteProperty $colName $Row[$colName]
	        }
	        $PS
          }
   }
}

################################################################

$script:SQLServer = "WIN8-01.corp.fabrikam.com"
$script:DBName = "ADGroupManagementDB"
$logfilepath = "C:\ADGroupManagement\logfile.txt"

if (-not (Test-Path $logFilePath)) 
{
    Write-Host "unable to find log file, creating new file in $logFilePath"  
    New-item $logFilePath -Force -ItemType file | Out-Null
}

$sqlAddGroups = @"
SELECT dbo.GroupTable.GroupGUID, dbo.GroupTable.GroupSamAccountname, dbo.GroupTable.UserGUID, 
dbo.GroupTable.StartDate, dbo.GroupTable.EndDate, dbo.UserTable.UserSamAccountName
FROM GroupTable
INNER JOIN UserTable
ON 
dbo.UserTable.UserGUID = dbo.GroupTable.UserGUID
WHERE dbo.GroupTable.StartDate <= GETDATE() 
AND dbo.GroupTable.EndDate >= GETDATE()
"@

$sqlRemoveGroups = @"
SELECT dbo.GroupTable.GroupGUID, dbo.GroupTable.GroupSamAccountname, dbo.GroupTable.UserGUID, 
dbo.GroupTable.StartDate, dbo.GroupTable.EndDate, dbo.UserTable.UserSamAccountName
FROM GroupTable
INNER JOIN UserTable
ON 
dbo.UserTable.UserGUID = dbo.GroupTable.UserGUID
WHERE StartDate <= GETDATE()
AND
EndDate <= GETDATE()
"@

Get-SQLData -strSQL $sqlAddGroups | ForEach-Object {

$strUser = [String]::Format("LDAP://<GUID={0}>",$_.UserGUID)
$User = [ADSI]$strUser

$strGroup = [String]::Format("LDAP://<GUID={0}>",$_.GroupGUID)
$Group = [ADSI]$strGroup

    if (-not $Group.member.Contains($User.DistinguishedName.ToString()))
    {
        try 
        {
        $Group.psbase.Invoke("Add",$User.Path)
        Add-Content -Value "$(get-date -f 'yyyy-MM-dd HH:mm:ss');Added $($User.DistinguishedName) To $($Group.DistinguishedName)" -Path $logFilePath
        }
        catch
        {
        return $_.exception
        }
    }
}

Get-SQLData -strSQL $sqlRemoveGroups | ForEach-Object {

$strUser = [String]::Format("LDAP://<GUID={0}>",$_.UserGUID)
$User = [ADSI]$strUser

$strGroup = [String]::Format("LDAP://<GUID={0}>",$_.GroupGUID)
$Group = [ADSI]$strGroup

    if ($Group.member.Contains($User.DistinguishedName.ToString()))
    {
        try
        {
        $Group.psbase.Invoke("Remove",$User.Path)
        Add-Content -Value "$(get-date -f 'yyyy-MM-dd HH:mm:ss');Removed $($User.DistinguishedName) From $($Group.DistinguishedName)" -Path $logFilePath
        }
        catch
        {
        return $_.exception
        }
    }
   
}


