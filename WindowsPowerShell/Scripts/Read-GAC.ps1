#requires -version 2.0
function Read-GAC {
  <#
    .SYNOPSIS
        Shows all assemblies or finds specified assembly in GAC.
    .OUTPUTS
        Array <Object[]>
    .NOTES
        Author: greg zakharov
  #>
  param(
    [Parameter(ValueFromPipeline=$true)]
    [String]$AssemblyName
  )
  
  [Collections.ArrayList]$al = New-Object Collections.ArrayList
  ($o = [Object].Assembly).GetType('Microsoft.Win32.Fusion').GetMethod(
    'ReadCache'
  ).Invoke($null, @(
    $al,
    $(if([String]::IsNullOrEmpty($AssemblyName)){$null}else{$AssemblyName}),
    $o.GetType('Microsoft.Win32.ASM_CACHE').GetField('GAC').GetValue($null)
  ))
  $al
}

