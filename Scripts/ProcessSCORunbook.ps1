param ($username,$password,$exeLoc,$newFolderName,$isRollback,$DBServer,$DBName,$SmConnection)
$error.Clear()
try
{

    import-module ($exeLoc + "\SCO-COM-Powershell.psm1")
    
    $connection = Connect-SCOServer -username $username -password $password
    $handle = $connection[0]
    $oismgr = $connection[1]
    
    [xml]$folderXml = Get-Content ($exeLoc + "\Folder.xml")

    $folderXml.Folder.Name.InnerText = $newFolderName

    if($isRollback -eq $true)
    {
        Remove-SCOFolder -Handle $handle -oismgr $oismgr -DeleteFromDB -FolderID ($folderXml.Folder.UniqueID.InnerText) -DBServer $DBServer -DBName $DBName
    }
    else
    {
        $folderID = Add-SCOFolder -Handle $handle -oismgr $oismgr -FolderXml $folderXml -RetainGUIDs $true -OverwriteExisting $true -DBServer $DBServer -DBName $DBName
    }

    $allRunbooks=get-childitem ($exeLoc + "\*.ois_export")

    foreach($file in $allRunbooks)
    {

        if($isRollback -eq $true)
        {
            [xml]$policy = Get-Content ($exeLoc + "\" + $file.Name)

            $policyID = $policy.ExportData.Policies.Folder.Policy.UniqueID.InnerText

            Remove-SCORunbook -Handle $handle -oismgr $oismgr -PolicyID $policyID -DeleteFromDB -DBServer $DBServer -DBName $DBName

        }
        else
        {
            $importedrunbook = Import-SCORunbookFile -Handle $handle -oismgr $oismgr -RunbookXmlFile ($exeLoc + "\" + $file.Name) -RetainGUIDs -OverwriteExisting -ParentFolderID $folderID -DBServer $DBServer -DBName $DBName -SMConnectionName $SmConnection
        }
    }
}
catch
{
    $error.ToString()
}
