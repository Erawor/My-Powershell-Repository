{
	# User wants the Usage Statement
	Usage;
	return;
}

get-attachmentfilterentry | remove-attachmentfilterentry
foreach ($file in $fileNamesToBlock) {add-attachmentfilterentry -Type: FileName -Name: $file}
foreach ($type in $contentTypesToBlock) {add-attachmentfilterentry -Type: ContentType -Name: $type}

