function ConvertFrom-DNSLogLine{
param(
    # Decorating input param to work with or without pipeline
    [Parameter(ValueFromPipeline=$true)]    
    [Alias( "DnsLogLine" )]                 
    [string[]]$InputObject                  
)
begin{
    # This isn't a magical incantation for scalable scripting
    # but rather the nasty regex needed to parse this particular log file

    $regexForServer2012PacketLines = `
      "(?<Raw_01_Date>^\d{1,2}/\d{1,2}/\d{4})\s(?<Raw_02_Time>\d{1,2}
[CA]\:\d{1,2}\:\d{1,2}\s\w{2})\s(?<Raw_03_TID>\S{3,4})\s(?<Raw_04_
[CA]Context>PACKET)\s+(?<Raw_05_IPI>\S+)\s(?<Raw_06_TCPUDP>[TCUDP]
[CA]{3})\s(?<Raw_07_SndRcv>[SndRcv]{3})\s(?<Raw_08_RemoteIP>\S+)\s*
[CA](?<Raw_09_XID>\S*)\s(?<Raw_10_QueryResponse>.)\s(?<Raw_11_OpCode>.)
[CA]\s+(?<Raw_121314_FlagsCombined>\[.+\])\s+(?<Raw_15_QuestionType>
[CA]\w+)\s+(?<Raw_16_QuestionName>.*)"

    # Instead of keeping copies of all the input objects in an array 
    # in order to look at information like ".Count" later on, we use 
    # integer counters instead. These can be incremented millions of times
    # and the size of the object remains the same
    $processedLinesCount = 0                
    $processedPacketLinesCount = 0          
}
process{
foreach( $DnsLogLine in $InputObject ){
    # This pattern of always using foreach inside process allows for graceful
    # handling of various scenarios with and without pipeline. Note that 
    # defining our input parameter as string[] rather than string is part
    # of what makes this work. Even if there is only one item we know we
    # can predictably iterate.

    $processedLinesCount++                  #3

    # Here we do our actual task work
    if( $_ -match $regexForServer2012PacketLines){

        $processedPacketLinesCount++

        $dateTimeStr = $matches.Raw_01_Date + " " + $matches.Raw_02_Time
        $matches[ "DateTime" ] = [datetime]::Parse( $dateTimeStr )
        $matches[ "RemoteIpAddr" ] = $matches.Raw_08_RemoteIP.trim()
        $matches[ "QuestionType" ]=$matches.Raw_15_QuestionType.trim()
        $qrySplit= [regex]::split($matches.Raw_16_QuestionName,'\(\d+\)')
        $matches[ "QueryFQDN" ] = [string]::Join(".",($qrySplit -ne ""))
	
        $matches
    }
}
}
end{
    Write-Verbose "Processed total lines: $ProcessedLinesCount"
    Write-Verbose "Processed PACKET lines: $processedPacketLinesCount"
}
}


