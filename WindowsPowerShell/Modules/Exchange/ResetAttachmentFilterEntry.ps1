# Synopsis: This script is used to reset the list of attachment-types that are
# blocked by the attachment-filtering feature in the Exchange Gateway role, to
# the factory set defaults.
#

function Usage()
{
@"

SUMMARY:

Exchange 2007 Gateway role implements the Attachment Filtering feature in the Gateway
role. This feature blocks attachments that are configured by the administrator, from
entering the Organization. The ResetAttachmentFilterEntry script returns the list
configuration (i.e. the kinds of attachments that are filtered) to the factory set
defaults. This script does not change any other settings for the attachment-filtering
feature.

USAGE:

ResetAttachmentFilterEntry.ps1 [-h | -?]

-h or -?`t`Displays this help screen.

Otherwise the attachment filtering list is reset to the default values.

EXAMPLES:

ResetAttachmentFilterEntry.ps1

"@
}

