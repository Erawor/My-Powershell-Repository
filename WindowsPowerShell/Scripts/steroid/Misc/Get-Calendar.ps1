#requires -version 2.0
function Get-Calendar {
  <#
    .SYNOPSIS
        Prints calendar for the given month.
    .EXAMPLE
        PS C:\> Get-Calendar -mf
    .EXAMPLE
        PS C:\> Get-Calendar -m 10 -y 2013 -mf
    .NOTES
        Author: greg zakharov
  #>
  param(
    [Parameter(Position=0)]
    [Alias('m')]
    [ValidateRange(1, 12)]
    [Int32]$Month = (Get-Date -u %m),
    
    [Parameter(Position=1)]
    [ValidateRange(2000, 9999)]
    [Int32]$Year = (Get-Date -u %Y),
    
    [Parameter(Position=2)]
    [Alias('mf')]
    [Switch]$MondayFirstly
  )
  
  begin {
    _from Globalization -import {
      DateTimeFormatInfo, CultureInfo
    }
    
    [DateTimeFormatInfo]::CurrentInfo.ShortestDayNames | % {$arr = @()}{$arr += $_}
    $cal = [CultureInfo]::CurrentCulture.Calendar
    $dow = [Int32]$cal.GetDayOfWeek([DateTime]([String]$Month + '.1.' + [String]$Year))
    
    if ($MondayFirstly) {
      $arr = $arr[1..$arr.Length] + $arr[0]
      if (($doe = --$dow) -lt 0) { $dow = 6 }
    }
  }
  process {
    $loc = [DateTimeFormatInfo]::CurrentInfo.MonthNames[$Month - 1] + ' ' + $Year
    $loc = [String]((' ' * [Math]::Round((20 - $loc.Length) / 2)) + $loc)
    
    if ($dow -ne 0) { for ($i = 0; $i -lt $dow; $i++) { $arr += (' ' * 2) } }
    1..$cal.GetDaysInMonth($Year, $Month) | % {
      if ($_.ToString().Length -eq 1) { $arr += ' ' + [String]$_ }
      else { $arr += [String]$_ }
    } #foreach
  }
  end {
    Write-Host $loc -fo Magenta
    for ($i = 0; $i -lt $arr.Length; $i += 6) {
      Write-Host $arr[$i..($i + 6)]
      $i++
    }
    ''
    _free {CultureInfo, DateTimeInformation}
  }
}

Export-ModuleMember -Function Get-Calendar

