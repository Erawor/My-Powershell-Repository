#requires -version 2.0
if (!(Test-Path alias:uptime)) {Set-Alias uptime Get-Uptime}

function Get-Uptime {
  <#
    .SYNOPSIS
        Gets system uptime.
    .OUTPUTS
        String
    .NOTES
        Author: greg zakharov
  #>
  
  $sti = struct SYSTEM_TIMEOFDAY_INFORMATION {
    Int64  'BootTime';
    Int64  'CurrentTime';
    Int64  'TimeZoneBias';
    UInt32 'TimeZoneId';
    UInt32 'Reserved';
    UInt64 'BootTimeBias';
    UInt64 'SleepTimeBias';
  }
  
  $sti = NtQuerySystemInformation $sti SystemTimeOfDayInformation
  
  '{0:D2}:{1:D2}:{2:D2} up {3} day{4}' -f (
    $u = (Get-Date) - [DateTime]::FromFileTime($sti.BootTime)
  ).Hours, $u.Minutes, $u.Seconds, $u.Days, $(if($u.Days -gt 1){'s'}else{''})
}

Export-ModuleMember -Alias uptime -Function Get-Uptime

