<#
Echo Server to emulate a server with the proper network add-ons installed for Port 7
#>
[cmdletbinding()]
Param()
[console]::Title = "Echo Server"
#Create the Listener port 
$Listener = New-Object System.Net.Sockets.TcpListener -ArgumentList 7

#Start the listener; opens up port 7 for incoming connections
$Listener.Start()
Write-Verbose "Server started"

While ($True) {
    <#
    Begin allowing TCPClient connections; this is a blocking call that will return a 
    TCPClient object when a connection has been made
    #>
    $incomingClient = $Listener.AcceptTcpClient()
    $remoteClient = $incomingClient.client.RemoteEndPoint.Address.IPAddressToString
    Write-Verbose ("New connection from {0}" -f $remoteClient)

    #Let it buffer for a second
    Start-Sleep -Milliseconds 1000

    #Get the data stream from connected client
    $stream = $incomingClient.GetStream()
    $activeConnection = $True
    Write-Verbose ("Connected to {0}" -f $remoteClient)
    While ($incomingClient.Connected) {    
        #Is there data available to process
        If ($Stream.DataAvailable) {
            Do {
                [byte[]]$byte = New-Object byte[] 1024
                Write-Verbose ("{0} Bytes Left from {1}" -f $return.Available,$remoteClient)
                $bytesReceived = $stream.Read($byte, 0, $byte.Length)
                If ($bytesReceived -gt 0) {
                    Write-Verbose ("{0} Bytes received from {1}" -f $bytesReceived,$remoteClient)
                    $String += [text.Encoding]::Ascii.GetString($byte[0..($bytesReceived - 1)])    
                } Else {
                    $activeConnection = $False
                    Break
                }   
            } While ($Stream.DataAvailable)
            If ($String) {
                Write-Host -Foreground Green -background Black `
                ("Message received from {0}:`n {1}" -f $remoteClient,$string) -Verbose

                #Resend the Data back to the client
                $bytes  = [text.Encoding]::Ascii.GetBytes($string)
                $string = $Null

                #Send the data back to the client
                Write-Verbose ("Echoing {0} bytes to {1}" -f $bytes.count, $remoteClient)
                $Stream.Write($bytes,0,$bytes.length)
                $stream.Flush()
                #$stream.Close()
            }
        }
    }
}
