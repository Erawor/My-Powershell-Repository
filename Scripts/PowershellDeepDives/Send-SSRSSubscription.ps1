
#========================================================
# Listing 1.6  Send-SSRSSubscription
#========================================================
function Send-SSRSSubscription {
[CmdletBinding()]
param(
        [Parameter(Mandatory=$True,
        ValueFromPipeline=$True)]
        [string[]]$Subscriptions,
        [Parameter(Mandatory=$True,
        ValueFromPipeline=$True)]
        [string]$ReportServer = $null,
        [string]$SmtpServer = "queryworks.local",
        [string]$ReportSender = "ssrs-powershell@queryworks.local",
	$TempFolder = $ENV:TEMP,
	$LogFile = "C:\Temp\SSRS PowerShell Subscription.log"
)
BEGIN {}         
PROCESS {  
        $item = $_
        if ( $item.ReportPath -eq $null ) { return }      

        $ts = Get-Date -Format "yyyyMMdd_hhmmt"
        $fileName = "$($item.LastName)_$($item.FirstName)_$($ts)"

        #message for display, or logging
        $msg = "$ts Report for $($item.FirstName) $($item.LastName) " 

        <# these variables need these to be declared
           because we are passing by reference 
           [ref] cannot be applied to a variable that does not exist.
        #>
        $mimeType = $null
        $encoding = $null
        $extension = $null
        $streamids = $null
        $warnings = $null       
        $params = $null

        $rv = New-Object Microsoft.Reporting.WinForms.ReportViewer
        $rv.ProcessingMode = "Remote";
        $rv.ServerReport.ReportServerUrl = $ReportServer
        $rv.ServerReport.ReportPath = $item.ReportPath
        [Microsoft.Reporting.WinForms.ReportParameter[]]$params = Get-SSRSParameterArray $item.ReportParameters             
        $rv.ServerReport.SetParameters($params)
        $rv.ProcessingMode = [Microsoft.Reporting.WinForms.ProcessingMode]::Remote

        #render the report
        $bytes = $rv.ServerReport.Render($item.ExportFormat, 
                                        $null, 
                                        [ref] $mimeType, 
                                        [ref] $encoding, 
                                        [ref] $extension, 
                                        [ref] $streamids, 
                                        [ref] $warnings)

        $FileName = "$($FileName).$($extension)"

        $msg += " $FileName "
        $file = Join-Path $TempFolder $FileName
        $fileStream = New-Object System.IO.FileStream($file, [System.IO.FileMode]::OpenOrCreate)
        $fileStream.Write($bytes, 0, $bytes.Length)
        $fileStream.Close()

        #deliver according to preference
        switch($item.Delivery)
        {
            "email" 
            {
                $msg += " - emailed to $($item.EmailAddress)"
                Send-MailMessage `
                    -SmtpServer "$SmtpServer" `
                    -To "$($item.EmailAddress)" `
                    -From "$ReportSender" `
                    -Subject "Report for $($item.FirstName) $($item.LastName) - $file - $ts" `
                    -Body "Report Generated" `
                    -Attachments $file
            
            }
            "sharedfolder" 
            {
                 $msg += "- saved to folder $($item.SharedFolder)"
                 Copy-Item  -Path $file -Destination $item.SharedFolder 
            }
        }
        
        Write-Verbose $msg
        Add-Content $LogFile -Value $msg

        #remove temp file
        Remove-Item -Path $file
        }
    END {}
}

