$ErrorActionPreference = "Continue"
$VerbosePreference = "Continue"
CLS
$webpage = "http://espn.go.com/college-football/schedule"
$wc = New-Object System.Net.WebClient;
[string] $content = $wc.DownloadString($webpage)
$games = @()
$anchor = "<td colspan=`"6`">"    #"</td></tr><tr class=`"colhead`">"
$counter = 1
$tokens = $content -split $anchor


$tokens[1..$tokens.Length] | %{
     write-verbose $tokens
    write-verbose $counter
	$date = $tokens[$counter].Substring($tokens[$counter].LastIndexOf('>')+1)
    $date = $tokens[$counter].Substring(0,$tokens[$counter].IndexOf('</td>')+1)
    $date = $date -replace "<", ""

    #write-verbose $date 
	$from = 1
	$sections = ($_ -split 'http://espn.go.com/college-football/team/_/id/')
    write-verbose $sections

write-verbose "----------------------Seperator Sections-------------------"


	while(($from+1 -lt $sections.Length) -and ($sections[$from+1] -notmatch 'game played at a neutral location')){
		$teams = $sections[$from],$sections[$from+1]
		#write-verbose $teams
        $i = $teams[0].IndexOf('>')+1
		#write-verbose $i 
        $j = $teams[0].IndexOf('<')
		$team1 = $teams[0].Substring($i,$j-$i)
        #write-verbose $team1
		$i = $teams[1].IndexOf('>')+1
		$j = $teams[1].IndexOf('<')
		$team2 = $teams[1].Substring($i,$j-$i)
        #write-verbose $team2
        #write-verbose $teams
write-verbose "----------------------Seperator teams0-------------------"

		write-verbose $teams[0]
write-verbose "----------------------Seperator teams1-------------------"

		write-verbose $teams[1]
      
write-verbose "----------------------Seperator token-------------------"
          
        $getleng = ($teams[1] -split "<tr class=`"evenrow team")[1]
        $leng = $getleng.Length

            If($leng -ge 1) { 
                        
                        $token = ($teams[1] -split "<tr class=`"evenrow team")[1]
                        $len = $token.Length
                        $Head = $token.LastIndexOf("`"><td>")
                        $Tail = $token.LastIndexOf("</td><td>")
                        $time = $token.Substring($Head,$Tail-$Head)
                        $time = $time -replace "`"><td>",""                              
            }
            Else {
                       
                        $token =  ($teams[1] -split "<tr class=`"oddrow team")[1]
                       write-output($token)

                       
                        $len = $token.Length
                        $Head = $token.LastIndexOf("`"><td>")
                        $Tail = $token.LastIndexOf("</td><td>")
                        $time = $token.Substring($Head,$Tail-$Head)
                        $time = $time -replace "`"><td>","" 

            
            }

        write-verbose $token

        $Head = $token.LastIndexOf("`"><td>")
        $Tail = $token.LastIndexOf("</td><td>")
        $time = $token.Substring($Head,$Tail-$Head)
        $time = $time -replace "`"><td>",""
        #write-verbose $token

		#$time = $token.Substring(0, $token.IndexOf('<'))
		$teams = "$team1 at $team2"
		$game = New-Object System.Object
		$game | Add-Member -type NoteProperty -name Teams -value $teams.Trim()
		$game | Add-Member -type NoteProperty -name Time -value $time.Trim()
		$game | Add-Member -type NoteProperty -name Date -value $date.Trim()
		$games += $game
		$from += 2
	}
	$counter++


}

$games | Export-Csv "C:\temp\OutPutNCAATest2.csv"  -NoTypeInformation
