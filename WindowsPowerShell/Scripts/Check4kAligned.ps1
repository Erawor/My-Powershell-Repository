#Internal private function to format result
Function Format-OSCResult
{
	Param
	(
		[bool]$is4kAligned,
		[string]$name,
		[string]$deviceID,
		[string]$diskSize,
		[string]$partitionSize,
		[string]$description
	)
	
	$deviceInfoList = @()
	
	$scriptInfo = New-Object PSObject
	$scriptInfo | Add-Member -MemberType NoteProperty -Name "Is4kAligned" -Value $is4kAligned
	$scriptInfo | Add-Member -MemberType NoteProperty -Name "Name" -Value $name
	$scriptInfo | Add-Member -MemberType NoteProperty -Name "DeviceID" -Value $deviceID
	$scriptInfo | Add-Member -MemberType NoteProperty -Name "DiskSize(GB)" -Value $diskSize
	$scriptInfo | Add-Member -MemberType NoteProperty -Name "PartitionSize(GB)" -Value $partitionSize
	$scriptInfo | Add-Member -MemberType NoteProperty -Name "Description" -Value $description
	
	$deviceInfoList += $scriptInfo
	
	$deviceInfoList
}

Function Test-OSC4kAligned
{
	<#
		.SYNOPSIS
			Test-OSC4kAligned is a function which can be used to check 4kb alignment issue.
		.DESCRIPTION
			Test-OSC4kAligned is a function which can be used to check 4kb alignment issue.
		.EXAMPLE
			PS C:\Users\TestUser> Test-OSC4kAligned
			  
		Is4kAligned Name                  DeviceID DiskSize(GB) PartitionSize(GB) Description            
		----------- ----                  -------- ------------ ----------------- -----------            
       		   True Disk #0, Partition #0          0.00         0.10              Installable File System
       		   True Disk #0, Partition #1 C:       125.36       125.36            Installable File System
       		   True Disk #0, Partition #2 D:       107.42       107.42            Installable File System
	#>
	
	#Initi variables
	$name = ""
	$deviceID = ""
	$partitionSize = 0
	$driveSize = 0
	$description = ""
	$deviceInfoList = @()

	#Get disk drives
	$diskDrives = Get-WmiObject -Class Win32_DiskDrive
	
	#foreach disks
	foreach($diskDrive in $diskDrives)
	{
		#Convert disk drive to disk partition
		$deviceID = $diskDrive.DeviceID -replace "\\","\\"
		$diskPartitions = Get-WmiObject -Query "ASSOCIATORS OF {Win32_DiskDrive.DeviceID=`"$deviceID`"} `
		WHERE AssocClass=Win32_DiskDriveToDiskPartition"
		
		foreach($diskPartition in $diskPartitions)
		{
			$is4kAligned = ($diskPartition.StartingOffset % 4096) -eq 0
			$name = $diskPartition.Name
			$partitionSize = "{0:0.00}" -f ($diskPartition.Size /1GB)
			$description = $diskPartition.Description
			
			#Convert disk partition to logical disk
			$query = "ASSOCIATORS OF {Win32_DiskPartition.DeviceID=`"$($diskPartition.DeviceID)`"} `
			WHERE AssocClass=Win32_LogicalDiskToPartition" 
			$logicalDisks = Get-WmiObject -Query $query
			
			foreach($logicalDisk in $logicalDisks)
			{
				$deviceID = $logicalDisk.DeviceID
				$diskSize = "{0:0.00}" -f ($logicalDisk.Size /1GB)
				$deviceInfoList += Format-OSCResult $is4kAligned $name $deviceID $diskSize $partitionSize $description
			}
		}
	}

	#Output result
	$deviceInfoList | Format-Table -AutoSize -Wrap
}
