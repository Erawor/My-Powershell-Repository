$overwrite = $false
$webClient = New-Object System.Net.WebClient 
$feeds =  (("Introduction_Highlights.wmv","http://video.ch9.ms/ch9/89dc/e9697fd1-58fd-4952-8074-89a5eb1a89dc/IntroHighlights_Source.wmv"),("Keynote_Rise_of_the_Humans_Regaining_Control_in_the_Digital_Age_.mp4","http://video.ch9.ms/ch9/e36c/2cbc724b-78b6-47cd-95f5-f97cb59fe36c/KeynoteDaveCoplin_high.mp4"),("Using_Recommendations_at_Scale_in_the_Cloud_Countdown_to_AzureML.mp4","http://video.ch9.ms/ch9/f1b2/72fb887a-caa1-471b-a125-df46cf86f1b2/RichardConwayPres_high.mp4"),("Scaling_your_mobile_and_Windows_applications_with_Azure_Notification_Hubs.mp4","http://video.ch9.ms/ch9/a77d/b5d6cde2-64c6-498a-8fd4-ab2a2f21a77d/RichardConwayPres_high.mp4"),("Get_Started_with_the_Cloud_Learning_Demos_for_New_Users_and_Startups.mp4","http://video.ch9.ms/ch9/b7a5/0882c649-cfea-43a0-91b5-31a5ccfab7a5/StevePlankPres_high.mp4"),("Building_Internet_of_Things_applications_with_Azure_and_Orleans.mp4","http://video.ch9.ms/ch9/1dd8/3bc17cc9-e146-41a1-bcf1-50d513c41dd8/RichardAstburyPres_high.mp4"),("Presentation_from_Startups_From_AWS_to_Azure.mp4","http://video.ch9.ms/ch9/5a02/66b08d0b-a08c-4265-a550-79cfa30f5a02/MickDelaneyPres_high.mp4"),("Presentation_from_Startups_Building_Zudio.mp4","http://video.ch9.ms/ch9/cb93/2ca693d7-7cfc-445d-a424-e5550527cb93/MarkRendlePres_high.mp4"))  
$entries = $feeds.Length
$progress = 0 
$pagepercent = 0 
$dir = get-location
foreach ($item in $feeds){
    $name,$link = $item
    $saveFileName = join-path $dir $name
    if ((-not $overwrite) -and (Test-Path -path $saveFileName))     
    {        
        write-progress -activity "$saveFileName already downloaded" -status "$pagepercent% ($progress / $entries) complete" -percentcomplete $pagepercent    
    }    
    else     
    {        
        write-progress -activity "Downloading $saveFileName" -status "$pagepercent% ($progress / $entries) complete" -percentcomplete $pagepercent       
        &{#TRY
            $webClient.DownloadFile($link, $saveFileName)
        }
        trap [Exception]{
            write-host
            write-host ("Unable to download " + $saveFileName)
            continue; 
        }
    }    
    $pagepercent = [Math]::floor((++$progress)/$entries*100) 
}
