Param(
  [Parameter(Mandatory = $true)]
  [string] $TopPublicFolder = "",
  [Parameter(Mandatory = $true)]
  [string] $User = ""
)

# This function validates the scripts parameters
function ValidateParams
{
  $validInputs = $true
  $errorString =  ""

  if ($TopPublicFolder -eq "")
  {
    $validInputs = $false
    $errorString += $RemoveUserFromPF_LocalizedStrings.res_0000
  }

  if ($User -eq "")
  {
    $validInputs = $false
    $errorString += $RemoveUserFromPF_LocalizedStrings.res_0001
  }

  if (!$validInputs)
  {
    Write-error "$errorString"
  }

  return $validInputs
}

####################################################################################################
# Script starts here
####################################################################################################

#load hashtable of localized string
Import-LocalizedData -BindingVariable RemoveUserFromPF_LocalizedStrings -FileName RemoveUserFromPFRecursive.strings.psd1

# Validate the parameters
$ifValidParams = ValidateParams;

if (!$ifValidParams) { exit; }

$getpfcmd = "get-publicfolder -identity ""$TopPublicFolder"" -Recurse -resultsize unlimited"

Invoke-Expression $getpfcmd | foreach {
	$permission = get-PublicFolderClientPermission -identity $_ -user $User -erroraction SilentlyContinue 

	if ($permission -ne $null) {
		remove-PublicFolderClientPermission -identity $_ -user $User -Confirm:$false
	}
}

