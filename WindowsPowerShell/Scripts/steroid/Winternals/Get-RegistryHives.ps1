#requires -version 2.0
function Get-RegistryHives {
  <#
    .SYNOPSIS
        Shows loaded registry hives.
    .NOTES
        Author: greg zakharov
  #>
  
  [IO.DriveInfo]::GetDrives() | ? {$_.DriveType -eq 'Fixed'} | % {
    $sb = New-Object Text.StringBuilder(256)
    $QueryDosDevice = delegate kernel32.dll QueryDosDeviceA UInt32 @(
      [String], [Text.StringBuilder], [Int32]
    )
    #\Device\HarddiskVolumeX storage
    $hdv = @{}
  }{
    if ($QueryDosDevice.Invoke(($$ = $_.Name.Substring(0, 2)), $sb, $sb.Capacity) -ne 0) {
      $hdv[$sb.ToString()] = $$
    }
  } #foreach
  
  $key = 'HKLM:\SYSTEM\CurrentControlSet\Control\hivelist'
  (Get-ItemProperty $key).PSObject.Properties | ? {
    $_.Name -notlike 'PS*'
  } | % {
    $rex = [Regex]'(?i:\A\\.*\\.*Volume\d)'
  }{
    $RegistryHives = New-Object PSObject -Property @{
      Name = ($$ = $_.Name).Substring(($i = $$.LastIndexOf('\') + 1), $$.Length - $i)
      Path = $(if (![String]::IsNullOrEmpty($_.Value)) {
        $_.Value.Replace(($$ = $rex.Match($_.Value).Value), $hdv[$$])
      } else { 'n/a' })
    }
    $RegistryHives.PSObject.TypeNames.Insert(0, 'RegistryHives')
    $RegistryHives
  } #foreach
}

Export-ModuleMember -Function Get-RegistryHives

