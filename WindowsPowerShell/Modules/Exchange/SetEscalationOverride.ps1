<#
    .SYNOPSIS
    SCM runs this script after a request to modify an override gets approved.
    Test as follows:
        New-CentralAdminOperation -name foo -workflow executescript -arguments @{Target="EXHB-99999"; Script="Scripts\SetEscalationOverride.ps1"; ScriptParameters=@{Identity="11112222-3333-4444-5555-AABBCCDDEEFF"; Expiration="12/1/2099"; Reason="changed my mind"} }

    .PARAMETER Identity
    ID of the override. Get this from Get-EscalationOverride.
    It is NOT the same as the ID of the SCM request!

    .PARAMETER Reason
    Optional explanation of why you are changing the override.
    Should come from SCM; right now, it doesn't.

    .PARAMETER Expiration
    Mandatory date/time when the override will expire.
 #>

[CmdletBinding()]
param
(
    # Won't get passed for right now.
    [Parameter(Mandatory = $false)]
    [System.Guid] $Identity,

    # Won't get passed for right now.
    # [Parameter(Mandatory = $false)]
    # [string] $Reason,

    [Parameter(Mandatory = $true)]
    [System.DateTime] $Expiration
)

# Add the Service Health snap-in.
Add-PSSnapin Microsoft.Exchange.Monitoring.ServiceHealth -ErrorAction SilentlyContinue

# Build the command line.
[string] $command = "Set-EscalationOverride"

if ($Identity -ne $null) { $command += " -Identity '$Identity'" }

# Reason won't get passed for right now.
# After SCM starts passing Reason, should be --->  if (-not [String]::IsNullOrEmpty($Reason)) { $command += " -Reason '$Reason'" }
if (-not [String]::IsNullOrEmpty($Reason))
{
    $command += " -Reason '$Reason'"
}
else
{
    $command += " -Reason 'Get reason from change request'"
}

if (-not [String]::IsNullOrEmpty($Expiration)) { $command += " -Expiration '$Expiration'" }

# For now, just write the command to the operation trace instead of executing it.
# Should be --->      & $command
Write-Host $command


