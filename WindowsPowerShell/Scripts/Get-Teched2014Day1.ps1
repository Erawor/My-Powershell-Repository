$overwrite = $false
$webClient = New-Object System.Net.WebClient 
$feeds =  (("TechEd_Keynote.mp4","http://media.ch9.ms/ch9/8319/4386938e-50ef-423c-961f-5a9356438319/KEY01_high.mp4"),("TechEd_101_An_Introduction_to_TechEd_repeats_on_May_11_at_4_00pm_.mp4","http://video.ch9.ms/sessions/teched/na/2014/TE101.mp4"),("Channel_9_Live_Final_TechEd_Countdown_Show.mp4","http://media.ch9.ms/ch9/26c7/0b451b7f-cd87-448d-8896-0227738f26c7/C902_high.mp4"),("Channel_9_Live_Cloud_for_IT_Professionals.mp4","http://media.ch9.ms/ch9/ff38/ebcf477e-7949-4394-916a-213353d4ff38/C903_high.mp4"),("Channel_9_Live_Wally_Mead_Then_and_Now.mp4","http://media.ch9.ms/ch9/dda7/09e0ae4c-fac1-4ac9-98a4-bcbb5151dda7/C904_high.mp4"),("Channel_9_Live_Client_Development_Q_A.mp4","http://media.ch9.ms/ch9/8d4b/3d2e4b6a-098c-4f9f-b8d2-b81fdc1f8d4b/C905_high.mp4"))  
$entries = $feeds.Length
$progress = 0 
$pagepercent = 0 
$dir = "D:\Videos\Teched2014\videos"
foreach ($item in $feeds){
    $name,$link = $item
    $saveFileName = join-path $dir $name
    if ((-not $overwrite) -and (Test-Path -path $saveFileName))     
    {        
        write-progress -activity "$saveFileName already downloaded" -status "$pagepercent% ($progress / $entries) complete" -percentcomplete $pagepercent    
    }    
    else     
    {        
        write-progress -activity "Downloading $saveFileName" -status "$pagepercent% ($progress / $entries) complete" -percentcomplete $pagepercent       
        &{#TRY
            $webClient.DownloadFile($link, $saveFileName)
        }
        trap [Exception]{
            write-host
            write-host ("Unable to download " + $saveFileName)
            continue; 
        }
    }    
    $pagepercent = [Math]::floor((++$progress)/$entries*100) 
}
