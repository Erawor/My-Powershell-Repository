#requires -version 2.0
if (!(Test-Path alias:procdump)) {Set-Alias procdump Write-ProcessDump}

function Write-ProcessDump {
  <#
    .SYNOPSIS
        Creates mini dump of the given process.
    .EXAMPLE
        PS C:\> Write-ProcessDump 332 -dt WithPrivateReadWriteMemory
    .EXAMPLE
        PS C:\> Write-ProcessDump $PID -dt ScanMemory -dn posh.dmp
    .NOTES
        Author: greg zakharov
  #>
  param(
    [Parameter(Mandatory=$true, Position=0, ValueFromPipeline=$true)]
    [Int32]$Id,
    
    [Parameter(Position=1)]
    [ValidateSet(
      'FilterMemory', 'FilterModulePaths', 'Normal', 'ScanMemory', 'WithCodeSegs',
      'WithDataSegs', 'WithFullMemory', 'WithFullMemoryInfo', 'WithHandleData',
      'WithIndirectlyReferencedMemory', 'WithoutOptionalData', 'WithPrivateReadWriteMemory',
      'WithProcessThreadData', 'WithThreadInfo', 'WithUnloadedModules'
    )]
    [Alias('dt')]
    [String]$DumpType = 'WithFullMemory',
    
    [Parameter(Position=2)]
    [Alias('dn')]
    [String]$DumpName = "$(Get-Date -u %d%m%Y_%H%M%S).dmp"
  )
  
  begin {
    $MiniDumpWriteDump = ($a = [PSObject].Assembly).GetType(
      "$($$ = 'System.Management.Automation.WindowsErrorReporting')$$+NativeMethods"
    ).GetMethod(
      'MiniDumpWriteDump', [Reflection.BindingFlags]40
    )
    
    $MiniDumpType = $a.GetType("$$+MiniDumpType")
  }
  process {
    try {
      $ps = [Diagnostics.Process]::GetProcessById($Id)
      $fs = [IO.File]::Create((Join-Path $pwd.Path $DumpName))
      $zp = [IntPtr]::Zero
      
      $ret = $MiniDumpWriteDump.Invoke($null, @(
        $ps.Handle, $ps.Id, $fs.SafeFileHandle, $MiniDumpType::"MiniDump$($DumpType)", $zp, $zp, $zp
      ))
    }
    catch {
      $_.Exception.InnerException
    }
    finally {
      if ($fs -ne $null) { $fs.Close() }
      if (!$ret) { Remove-Item $DumpName -ea 0 }
    }
  }
  end {}
}

Export-ModuleMember -Alias procdump -Function Write-ProcessDump

